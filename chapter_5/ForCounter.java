//counter controlled loop iteration using `for` for iteration specifies the counter controlled iteration in a single statement

public class ForCounter{
	public static void main(String[] args) {
		for (int counter = 1; counter <=10; counter = counter + 1)/*statement header*/ {
			System.out.println("your numbers are: " + counter);
		}
		for (int j=2; j <=20; j++) {
			System.out.println(" " + j);
		}
		
	}
}